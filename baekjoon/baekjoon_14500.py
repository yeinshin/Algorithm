#https://www.acmicpc.net/problem/14500
#14500번-테트로미노
n,m = map(int,input().split())
graph = [list(map(int,input().split())) for _ in range(n)]
t = [
    [(0,0), (0,1), (1,0), (1,1)], # ㅁ
    [(0,0), (0,1), (0,2), (0,3)], # ㅡ
    [(0,0), (1,0), (2,0), (3,0)], # ㅣ
    [(0,0), (0,1), (0,2), (1,0)], 
    [(1,0), (1,1), (1,2), (0,2)],
    [(0,0), (1,0), (1,1), (1,2)], # ㄴ
    [(0,0), (0,1), (0,2), (1,2)], # ㄱ
    [(0,0), (1,0), (2,0), (2,1)],
    [(2,0), (2,1), (1,1), (0,1)],
    [(0,0), (0,1), (1,0), (2,0)], 
    [(0,0), (0,1), (1,1), (2,1)],
    [(0,0), (0,1), (0,2), (1,1)], # ㅜ
    [(1,0), (1,1), (1,2), (0,1)], # ㅗ
    [(0,0), (1,0), (2,0), (1,1)], # ㅏ
    [(1,0), (0,1), (1,1), (2,1)], # ㅓ
    [(1,0), (2,0), (0,1), (1,1)],
    [(0,0), (1,0), (1,1), (2,1)],
    [(1,0), (0,1), (1,1), (0,2)],
    [(0,0), (0,1), (1,1), (1,2)]
]
result = 0
def check (x,y):
    global result 
    for i in range(19):
        cnt = 0
        for j in range(4):
            nx = x+t[i][j][0]
            ny = y+t[i][j][1]
            if 0<=nx<n and 0<=ny<m:
                cnt+=graph[nx][ny]
        result = max(result,cnt)

for i in range(n):
    for j in range(m):
        check(i,j)
print(result)